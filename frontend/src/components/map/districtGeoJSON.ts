// Convert [lat,lng] array to GeoJSON Feature
export function toFeature(key: string, coords: number[][], props: Record<string, any>) {
  return {
    type: 'Feature' as const,
    properties: { key, ...props },
    geometry: {
      type: 'Polygon' as const,
      coordinates: [coords.map(([lat, lng]) => [lng, lat])],
    },
  }
}
