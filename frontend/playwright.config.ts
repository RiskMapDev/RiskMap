import { defineConfig, devices } from "@playwright/test";

/**
 * Сквозные тесты идут против живых серверов.
 *
 * Моков нет намеренно: проверяется не вёрстка, а то, что интерфейс показывает
 * те же числа, которые лежат в базе. Подменённый ответ API проверял бы только
 * умение React отрисовать переданный объект, а это уже покрыто модульными
 * тестами в `src/**` и никакого отношения к приёмке ТЗ не имеет.
 *
 * Адреса берутся из окружения: развёртывание на другом порту не должно
 * требовать правки тестов.
 *   E2E_BASE_URL — интерфейс Next.js       (по умолчанию 127.0.0.1:3001)
 *   E2E_API_URL  — API FastAPI с префиксом (по умолчанию 127.0.0.1:8100/api/v1)
 */
const baseURL = process.env.E2E_BASE_URL ?? "http://127.0.0.1:3001";

export default defineConfig({
  testDir: "./e2e",

  /*
    `webServer` отсутствует сознательно.

    На Windows `next start` на занятом порту завершается молча, и Playwright
    ждал бы готовности уже поднятого чужого сервера, считая его своим. Тогда
    тесты шли бы против сборки, которой в этом прогоне никто не собирал.
    Серверы поднимаются снаружи, а тесты сразу говорят, если их нет, — см.
    `e2e/global-setup.ts`.
  */
  globalSetup: "./e2e/global-setup.ts",

  /*
    Один рабочий процесс.

    Причина не в скорости, а в общем состоянии: база одна на всех, вход одного
    и того же демо-пользователя в двух процессах пишет в `users.last_login_at`
    и в журнал действий, а параллельные обращения к территориальным выборкам
    заметно замедляют PostGIS. Числа, которые проверяют тесты, должны
    оставаться теми же на протяжении прогона.
  */
  workers: 1,
  fullyParallel: false,

  /* Повторов нет: тест, проходящий со второй попытки, скрывает гонку, а не
     чинит её. Падение обязано быть видно сразу. */
  retries: 0,

  /* Норматив ТЗ 20 на отклик — секунды, а не минуты. Долгое ожидание здесь
     маскировало бы нарушение норматива вместо того, чтобы его показать. */
  timeout: 60_000,
  expect: { timeout: 15_000 },

  reporter: process.env.CI ? [["list"], ["html", { open: "never" }]] : [["list"]],

  use: {
    baseURL,
    locale: "ru-RU",
    timezoneId: "Asia/Almaty",
    actionTimeout: 15_000,
    navigationTimeout: 30_000,
    trace: "retain-on-failure",
    screenshot: "only-on-failure",
    video: "off",
  },

  projects: [
    {
      /*
        Основное разрешение — 1440×900: на нём открыты обе боковые панели
        (сайдбар и панель слоёв карты), то есть виден весь интерфейс, который
        описывает ТЗ. Проверки на 1024×768 и 390×844 живут в
        `responsive.spec.ts` и задают размер окна сами.
      */
      name: "desktop-1440",
      use: { ...devices["Desktop Chrome"], viewport: { width: 1440, height: 900 } },
    },
  ],
});
